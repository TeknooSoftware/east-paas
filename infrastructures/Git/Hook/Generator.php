declare(strict_types=1);

namespace Teknoo\East\Paas\Infrastructures\Git\Hook;

use Closure;
use Teknoo\East\Paas\Infrastructures\Git\Hook;
use Teknoo\States\State\StateInterface;
use Teknoo\States\State\StateTrait;

/**
 * @Mixin Hook
 *
 * @copyright   Copyright (c) EIRL Richard Déloge (https://deloge.io - richard@deloge.io)
 * @copyright   Copyright (c) SASU Teknoo Software (https://teknoo.software - contact@teknoo.software)
 * @license     http://teknoo.software/license/mit         MIT License
 * @author      Richard Déloge <richard@teknoo.software>
 */
class Generator implements StateInterface
{
    use StateTrait;

    private function update(): Closure
    {
        return function (string $key, mixed $value): Hook {
            $this->{$key} = $value;

            $this->updateStates();

            return $this;
        };
    }
}
