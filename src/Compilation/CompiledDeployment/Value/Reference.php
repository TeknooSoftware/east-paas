namespace Teknoo\East\Paas\Compilation\CompiledDeployment\Value;

use Stringable;

/**
 * Immutable value object to help transcriber to detect a reference in the default bag or a normal value
 * (A "normal' value must be scalar)
 *
 * @copyright   Copyright (c) EIRL Richard Déloge (https://deloge.io - richard@deloge.io)
 * @copyright   Copyright (c) SASU Teknoo Software (https://teknoo.software - contact@teknoo.software)
 * @license     http://teknoo.software/license/bsd-3         3-Clause BSD License
 * @author      Richard Déloge <richard@teknoo.software>
 */
class Reference implements Stringable
{
    public function __construct(
        private readonly string $reference,
    ) {
    }

    public function __toString(): string
    {
        return $this->reference;
    }
}
